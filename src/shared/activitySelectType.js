export class ActivityType {
    static isPilates(type) {
        return type === "pilates";
    }

    static isYoga(type) {
        return type === "yoga";
    }

    static isJogging(type) {
        return type === "jogging";
    }

    static isZumba(type) {
        return type === "zumba";
    }

    static isAbs(type) {
        return type === "abs";
    }
}
